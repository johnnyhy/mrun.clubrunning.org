	<h1>Cross Country</h1>

	<p><img src="CrossCountryHeader.png" alt="" width="100%"/></p>

	<p>In the fall, MRun competes in <a href="http://clubrunning.org" target="_blank">National Intercollegiate Running Club Association</a> (NIRCA) cross country races. There are no tryouts, cuts, or qualifying times for the team, so anyone who wants to race is welcome! Most races are 8k for men and 6k for women, with a few 5k races sprinkled in.</p>

	<p>MRun travels to several local NIRCA meets in the fall, as well as a select few collegiate varsity races. See the schedules tab for more information on races.</p>

	<p>MRun enjoys a reputation as one of the most competitive clubs in the nation. The men and women have finished very well in the National Championships every year since the inception of NIRCA. The women were National Champions in 2008, while the men's team brought home the gold in 2010.</p> 


