	<h1>Track &amp; Field</h1>

	<p><img src="TrackHeader.png" alt="" width="100%" /></p>

	<p>MRun has several different training groups to prepare for the races, with the training plans being included in the weekly training e-mails sent out by the training chair. There are separate training plans for sprints (100-400, hurdles), mid-distance (800-Mile), and distance (3k steeplechase-10k, half marathon), as well as training opportunities for field events (shotput, discus, long jump, high jump, triple jump).</p>

	<h2>Sprint Training Plan</h2>

	<p>Sprint practices in the fall will consist of doing short runs with the distance team at 4:15 at the CCRB, and Tuesday/Thursday track workouts at 8:30pm at the Varsity Outdoor Track. More focused sprint training will begin when we return from winter break.</p>

	<h2>Distance/Mid-Distance Training Plan</h2>

	<p>Workouts will start in January on Tuesday and Thursday nights at 8:30 on the varsity indoor track.</p>

	<h2>Field Event Training Plan</h2>

	<p>Questions should be directed to the <a href="http://mrun.clubrunning.org/about/contact.php">sprint training chair</a>.</p>

