          <h2 class="featurette-heading">So Who Are We?</h2>
          <p class="lead">aside from the leaders and best...</p>
          <p>...MRun is the official running club of the University of Michigan. One of the founding clubs in the club running scene, MRun has been a dominant force since its inception in 2004 and has amassed to an overall membership of over 200 dues-paying undergraduate and graduate students in Ann Arbor. This makes MRun not only the biggest, most popular, and fastest growing club sport on campus, but also the largest collegiate running club in the nation.</p>
		  
          <h2 class="featurette-heading">How Do We Compete?</h2>
          <p>Competing in both varsity and club cross-country and track and field meets across the country, MRun is very competitive in the collegiate running world. The club competes at high levels and wins races at distances ranging between the 5k and marathon. Last year, the MRun women placed third overall at the XC nationals. In 2014 MRun celebrated two fantastic finishes, as the men's team won silver, and the women's team won gold at the NIRCA XC Championships. The MRun men were national champions at the 2010 NIRCA Cross-Country Championships, while the women completed the same feat in 2008.</p>

          <p>In addition to distance running, MRun also caters to track and field athletes and has year-round training for sprinters and field competitors. Because MRun competes in both indoor and outdoor track, athletes in distance, sprint, and field events are able to travel around the Midwest and compete in a long winter and spring track season against both club and varsity athletes. The MRun men and women have had many individual top-five finishes in their respective events at the NIRCA Track and Field National Championships, as well as many great overall team performances. In 2016, the MRun women placed third overall in both the Half Marathon and Track and Field Championships. In 2015, the men and women both got the bronze in T&amp;F, while the women took home the gold in the Half Marathon.</p>

          <h2 class="featurette-heading">Is MRun Right For Me?</h2>
          <p>Although MRun is very competitive, we accomodate runners and athletes of any type, and thus all ability levels are welcome. There are no cuts, tryouts, or time trials necessary to join the club. Whether you wish to compete against colleges nationwide, run in actual varsity races, or just show up a few times a week to run and hang out with friends, MRun is the right club for you!</p>
          
          <p>MRun has a very social atmosphere, which is inviting to newcomers and is one of the reasons why so many friendships are built within the club. Also, MRun provides more than just an outlet for running and friendships: it is a large charitable organization, deeply involved within the community. This past year alone, the club as a whole raised over $17,000 for various local and national charities! Some of the charities and organizations that we worked with include the Special Olympics, The American Cancer Society, and myTeam Triumph.</p>
		  
